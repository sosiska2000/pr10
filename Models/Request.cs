using System; // Использование базовых классов .NET (не используется явно, но для базовой функциональности)
using System.Collections.Generic; // Использование коллекций List<T>
using System.Linq; // Использование LINQ-методов (хотя в данном классе не используется явно)
using System.Text; // Использование классов для работы с кодировкой (не используется явно в этом файле)
using System.Threading.Tasks; // Использование асинхронного программирования (не используется явно в этом файле)

namespace APIGigaChat.Models // Пространство имен для моделей запросов API GigaChat
{
    // Основной класс для создания запросов к API GigaChat
    public class Request
    {
        // Название модели ИИ, которую следует использовать для генерации ответа
        // Например: "GigaChat", "GigaChat-Plus" и т.д.
        public string model { get; set; }

        // Список сообщений, представляющих историю диалога
        // Каждое сообщение имеет роль (role) и содержание (content)
        // Этот параметр передает контекст разговора модели
        public List<Message> messages { get; set; }

        // Флаг, указывающий, должен ли ответ приходить в потоковом режиме
        // true - ответ будет приходить частями (streaming)
        // false - ответ придет одним полным сообщением (режим по умолчанию)
        public bool stream { get; set; }

        // Коэффициент штрафа за повторения (penalty for repetition)
        // Значение больше 1 уменьшает вероятность повторения одних и тех же фраз
        // 1 - нет штрафа, >1 - усиление штрафа за повторения
        // Используется для повышения разнообразия генерируемого текста
        public int repetition_penalty { get; set; }

        // Вложенный класс, представляющий одно сообщение в диалоге
        // Используется как в запросах (от пользователя), так и в ответах (от ассистента)
        public class Message
        {
            // Роль отправителя сообщения в диалоге
            // Возможные значения:
            // - "user": сообщение от пользователя
            // - "assistant": ответ от ИИ-ассистента
            // - "system": системное сообщение для задания поведения модели
            public string role { get; set; }

            // Текстовое содержание сообщения
            // Содержит фактический текст, который сказал пользователь или ассистент
            public string content { get; set; }
        }
    }
}